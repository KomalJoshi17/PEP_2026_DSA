/*
    A graph G = (V,E) consist of:
        V -> set of vertices (nodes) -> an individual object in graph
        E -> set of edges connecting pairs of vertices -> 

    Graph is not a non linear but a network based data struture
        
    Directed graph -> edges have direction (linkedIn, instagram)
    Undirected graph -> edges have no direction

    Weighted graph -> edges have values (weight/distance/cost)
    Unweighted graph -> edges have no values (by default its 1)

    Cyclic graph -> when a point is repeating twice while traversing / graph that have cycle --> a path that start and end at the same vertex
    Acyclic graph -> when graph does not have cycle
    Path -> a sequence of vertices connected edges

    Degree -> how many other nodes are connected to a particular node (undirected) 
              Directed graph :-
                            in degree -> incomming edges (sink) 
                            out degree -> outgoing edges (source)

    Graph can be discontinous
    A tree can be a graph but a graph can not be a tree
*/